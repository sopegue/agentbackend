    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        try {
            $prop = Propertie::find($request->prop);
            if ($prop) {
                $report = new Report();
                $report->property_id = $request->prop;
                $report->signal = $request->signal;
                $report->content = $request->content;
                $report->save();
                return [
                    'message' => 'property has been reported',
                    'status' => '201'
                ];
            }
            return [
                'message' => 'not found',
                'status' => '404'
            ];
        } catch (\Throwable $th) {
            return [
                'message' => 'error',
                'status' => '500'
            ];
        }
    }

